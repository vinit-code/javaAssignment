package assignmentSolutions.javaBasics_1;

class Que4 {
    public void solve() {
        // int variable
        int age = 21;
        System.out.println(age);

        // Boolean variable
        boolean flag = false;
        System.out.println(flag);

        // char variable
        char ch = 'V';
        System.out.println(ch);

        // float variable
        float rate = 7.50f;
        System.out.println(rate);

        // double variable
        double fraction = 4.33d;
        System.out.println(fraction);

        
    }    
}
